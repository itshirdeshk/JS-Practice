// While
let index = 0;
while(index <= 10){
    console.log(`Value of index is ${index}`);
    index = index + 2;
}

let array = [1, 2, 3]
let i = 0;
while(i < array.length){
    console.log(`Value is ${array[i]}`);
    i++;
}

let score = 1;
do{
    console.log(`Score is ${score}`);
    score++;
} while(score <= 10);